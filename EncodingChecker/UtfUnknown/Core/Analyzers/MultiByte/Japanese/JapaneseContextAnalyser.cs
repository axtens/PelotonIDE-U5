    private int[] relSample = new int[6];
    private int totalRel;
    private int lastCharOrder;
    private int needToSkipCharNum;
    private bool done;

    public JapaneseContextAnalyser() => this.Reset();

    public float GetConfidence() => this.totalRel > 4 ? (float) (this.totalRel - this.relSample[0]) / (float) this.totalRel : -1f;

    public void HandleData(byte[] buf, int offset, int len)
    {
      int num = offset + len;
      if (this.done)
        return;
      int offset1 = this.needToSkipCharNum + offset;
      while (offset1 < num)
      {
        int charLen;
        int order = this.GetOrder(buf, offset1, out charLen);
        offset1 += charLen;
        if (offset1 > num)
        {
          this.needToSkipCharNum = offset1 - num;
          this.lastCharOrder = -1;
        }
        else
        {
          if (order != -1 && this.lastCharOrder != -1)
          {
            ++this.totalRel;
            if (this.totalRel > 1000)
            {
              this.done = true;
              break;
            }
            ++this.relSample[(int) JapaneseContextAnalyser.jp2CharContext[this.lastCharOrder, order]];
          }
          this.lastCharOrder = order;
        }
      }
    }

    public void HandleOneChar(byte[] buf, int offset, int charLen)
    {
      if (this.totalRel > 1000)
        this.done = true;
      if (this.done)
        return;
      int index = charLen == 2 ? this.GetOrder(buf, offset) : -1;
      if (index != -1 && this.lastCharOrder != -1)
      {
        ++this.totalRel;
        ++this.relSample[(int) JapaneseContextAnalyser.jp2CharContext[this.lastCharOrder, index]];
      }
      this.lastCharOrder = index;
    }

    public void Reset()
    {
      this.totalRel = 0;
      for (int index = 0; index < 6; ++index)
      {
        this.relSample[index] = 0;
        this.needToSkipCharNum = 0;
        this.lastCharOrder = -1;
        this.done = false;
      }
    }

    protected abstract int GetOrder(byte[] buf, int offset, out int charLen);

    protected abstract int GetOrder(byte[] buf, int offset);

    public bool GotEnoughData() => this.totalRel > 100;
